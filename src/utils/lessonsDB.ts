export const getLessons = () => lessons
export const addLesson = (lesson: ILessons) => lessons.push(lesson)

export const deleteLesson = (id: number) => {
  lessons.filter((lesson) => lesson.id !== id)
}

export const updateLesson = (
  id: number,
  lessonName: string,
  language:
    | "Java"
    | "Python"
    | "Kotlin"
    | "C++"
    | "Scratch"
    | "Literacy"
    | "Android"
    | "JavaScript"
    | "Frontend",
  title: string,
  videoLink: string,
  level: "begin" | "free" | "advanced" | "medium"
) => {
  const lesson = lessons.find((lesson) => lesson.id === id)
  if (lesson) {
    lesson.language = language
    lesson.lessonName = lessonName
    lesson.level = level
    lesson.title = title
    lesson.videoLink = videoLink
  } else {
    throw new Error("NO CATEGORY FOUND")
  }
}

export const getByID = (id: number) =>
  lessons.find((lesson) => lesson.id === id)
